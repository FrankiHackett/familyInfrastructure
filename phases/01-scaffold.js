import { mkdirSync, existsSync, cpSync, writeFileSync, readdirSync, statSync } from 'node:fs'
import { join, basename } from 'node:path'
import { logger } from '../lib/logger.js'
import { exec, spawn } from '../lib/exec.js'
import { openInEditor, confirm } from '../lib/prompt.js'

// Generate a timestamp prefix for the migration file
function migrationTimestamp() {
  return new Date().toISOString().replace(/[-T:.Z]/g, '').slice(0, 14)
}

// Build an initial migration SQL from the user's inputs.
// Creates the schema namespace, standard columns, RLS boilerplate, and indexes.
// Domain-specific columns are left as TODOs for the user to fill in.
function buildMigrationSql(inputs) {
  const schema = inputs.appName.replace(/-/g, '_')
  const table  = `${schema}_entries` // placeholder table name — user should rename
  const { accessModel } = inputs

  const rlsPolicies = {
    personal: `
-- SELECT
CREATE POLICY "${table}_select"
  ON ${schema}.${table} FOR SELECT
  USING (auth.uid() = user_id);

-- INSERT
CREATE POLICY "${table}_insert"
  ON ${schema}.${table} FOR INSERT
  WITH CHECK (auth.uid() = user_id);

-- UPDATE
CREATE POLICY "${table}_update"
  ON ${schema}.${table} FOR UPDATE
  USING (auth.uid() = user_id)
  WITH CHECK (auth.uid() = user_id);

-- DELETE
CREATE POLICY "${table}_delete"
  ON ${schema}.${table} FOR DELETE
  USING (auth.uid() = user_id);`,

    shared: `
-- SELECT — any household member
CREATE POLICY "${table}_select"
  ON ${schema}.${table} FOR SELECT
  USING (
    household_id IN (
      SELECT household_id FROM public.household_members WHERE user_id = auth.uid()
    )
  );

-- INSERT
CREATE POLICY "${table}_insert"
  ON ${schema}.${table} FOR INSERT
  WITH CHECK (
    auth.uid() = user_id AND
    household_id IN (
      SELECT household_id FROM public.household_members WHERE user_id = auth.uid()
    )
  );

-- UPDATE
CREATE POLICY "${table}_update"
  ON ${schema}.${table} FOR UPDATE
  USING (
    household_id IN (
      SELECT household_id FROM public.household_members WHERE user_id = auth.uid()
    )
  );

-- DELETE (creator only)
CREATE POLICY "${table}_delete"
  ON ${schema}.${table} FOR DELETE
  USING (auth.uid() = user_id);`,

    partner: `
-- SELECT — owner or designated partner
CREATE POLICY "${table}_select"
  ON ${schema}.${table} FOR SELECT
  USING (auth.uid() = user_id OR auth.uid() = partner_id);

-- INSERT — owner only
CREATE POLICY "${table}_insert"
  ON ${schema}.${table} FOR INSERT
  WITH CHECK (auth.uid() = user_id);

-- UPDATE — owner or partner
CREATE POLICY "${table}_update"
  ON ${schema}.${table} FOR UPDATE
  USING (auth.uid() = user_id OR auth.uid() = partner_id);

-- DELETE — owner only
CREATE POLICY "${table}_delete"
  ON ${schema}.${table} FOR DELETE
  USING (auth.uid() = user_id);`,
  }

  const partnerColumn = accessModel === 'partner'
    ? '\n  partner_id     uuid REFERENCES auth.users(id) ON DELETE SET NULL,'
    : ''
  const householdColumn = accessModel === 'shared'
    ? '\n  household_id   uuid NOT NULL,'
    : ''

  return `-- =============================================================
-- Migration: Initial schema for ${inputs.appName}
-- Schema namespace: ${schema}
-- Access model: ${accessModel}
-- Schema description: ${inputs.schemaDescription || 'N/A'}
-- Generated by household-app-infrastructure
-- =============================================================
-- REVIEW THIS FILE before applying. Rename the table, add domain
-- columns, and remove any placeholder sections.
-- =============================================================

-- 1. Schema namespace
CREATE SCHEMA IF NOT EXISTS ${schema};

-- 2. Extensions
CREATE EXTENSION IF NOT EXISTS "uuid-ossp";
CREATE EXTENSION IF NOT EXISTS moddatetime;

-- 3. Table
-- TODO: Rename "${table}" to your actual entity name.
-- TODO: Replace the placeholder domain columns with your actual columns.
CREATE TABLE ${schema}.${table} (
  id             uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id        uuid NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,${partnerColumn}${householdColumn}
  created_at     timestamptz NOT NULL DEFAULT now(),
  updated_at     timestamptz NOT NULL DEFAULT now(),

  -- TODO: Add your domain-specific columns here.
  -- Examples from your description: "${inputs.schemaDescription || 'add columns here'}"
  -- name          text NOT NULL,
  -- value         numeric,
  -- status        text CHECK (status IN ('active', 'archived')),
  notes          text
);

-- 4. Indexes
CREATE INDEX idx_${table}_user_id   ON ${schema}.${table}(user_id);
CREATE INDEX idx_${table}_created   ON ${schema}.${table}(created_at DESC);

-- 5. Updated-at trigger
CREATE TRIGGER set_${table}_updated_at
  BEFORE UPDATE ON ${schema}.${table}
  FOR EACH ROW EXECUTE FUNCTION moddatetime(updated_at);

-- 6. Enable RLS
ALTER TABLE ${schema}.${table} ENABLE ROW LEVEL SECURITY;

-- 7. RLS Policies (${accessModel} model)
${rlsPolicies[accessModel]}

-- 8. Grant schema access to authenticated role
GRANT USAGE ON SCHEMA ${schema} TO authenticated;
GRANT ALL ON ALL TABLES IN SCHEMA ${schema} TO authenticated;
GRANT ALL ON ALL SEQUENCES IN SCHEMA ${schema} TO authenticated;
`
}

export async function runScaffold(inputs) {
  logger.phase('1', 'Scaffold')

  const { appName, appDir, codePath, services, schemaDescription } = inputs
  const schema = appName.replace(/-/g, '_')

  // 1a. Create Vite+React project
  if (existsSync(appDir)) {
    if (!statSync(appDir).isDirectory()) {
      throw new Error(`App directory path points to a file, not a directory: ${appDir}\nDid you mean to use this as the source code path instead?`)
    }
    logger.warn(`Directory already exists: ${appDir}`)
    const ok = await confirm(`Continue and use existing directory?`)
    if (!ok) throw new Error('Scaffold aborted — directory exists.')
  } else {
    logger.step(`Creating Vite+React project at ${appDir}`)
    const parentDir = join(appDir, '..')
    mkdirSync(parentDir, { recursive: true })
    await spawn('npm', ['create', 'vite@latest', appName, '--', '--template', 'react-ts'], {
      cwd: parentDir,
      env: { CI: '1' },
    })
    logger.success('Vite project created')
  }

  // 1b. Install base dependencies
  logger.step('Installing base dependencies')
  const baseDeps = ['@supabase/supabase-js']
  const baseDevDeps = [
    'vitest', '@vitest/coverage-v8', '@vitejs/plugin-react',
    '@testing-library/react', '@testing-library/user-event',
    'msw', '@playwright/test',
  ]
  if (services.includes('anthropic')) baseDeps.push('@anthropic-ai/sdk')
  if (services.includes('resend'))    baseDeps.push('resend')

  await spawn('npm', ['install', ...baseDeps], { cwd: appDir })
  await spawn('npm', ['install', '-D', ...baseDevDeps], { cwd: appDir })
  logger.success('Dependencies installed')

  // 1c. Insert app code
  if (codePath && existsSync(codePath)) {
    logger.step(`Inserting app code from ${codePath}`)
    const srcDest = join(appDir, 'src')
    if (statSync(codePath).isDirectory()) {
      const entries = readdirSync(codePath, { withFileTypes: true })
      for (const entry of entries) {
        const src = join(codePath, entry.name)
        const dest = join(srcDest, entry.name)
        cpSync(src, dest, { recursive: true })
      }
    } else {
      // Single file — copy it directly into src/
      cpSync(codePath, join(srcDest, basename(codePath)))
    }
    logger.success('App code inserted into src/')
  }

  // 1d. Create migrations directory — use supabase/migrations/ so the supabase CLI
  //     can find them without extra flags. Also create a symlink at migrations/ for
  //     direct browsing convenience.
  const migrationsDir = join(appDir, 'supabase', 'migrations')
  mkdirSync(migrationsDir, { recursive: true })
  logger.success('supabase/migrations/ directory created')

  // 1e. Generate initial migration (only if supabase)
  if (services.includes('supabase')) {
    const ts = migrationTimestamp()
    const migrationFile = join(migrationsDir, `${ts}_init_${schema}.sql`)
    const sql = buildMigrationSql(inputs)
    writeFileSync(migrationFile, sql, 'utf-8')
    logger.success(`Migration file created: supabase/migrations/${basename(migrationFile)}`)

    // Open for review
    logger.warn('Review and edit the migration before it is applied in Phase 3.')
    const edit = await confirm(`Open migration in $EDITOR now?`)
    if (edit) await openInEditor(migrationFile)

    inputs._migrationFile = migrationFile
  }

  // 1f. Initialise git
  if (!existsSync(join(appDir, '.git'))) {
    logger.step('Initialising git repository')
    await exec('git init && git checkout -b main', { cwd: appDir })
    logger.success('Git initialised on branch main')
  }

  // Write .gitignore
  const gitignore = [
    '# Dependencies', 'node_modules/', '',
    '# Build output', 'dist/', 'build/', '.next/', '',
    '# Env files — never commit', '.env.local', '.env.*.local', '.env.production', '',
    '# Bootstrap config', '.bootstrap-config.json', '',
    '# Test output', 'coverage/', 'playwright-report/', 'test-results/', '',
    '# Editor', '.DS_Store', '*.swp', '',
  ].join('\n')
  writeFileSync(join(appDir, '.gitignore'), gitignore, 'utf-8')

  // Write .env.local.template
  const envTemplate = buildEnvTemplate(inputs)
  writeFileSync(join(appDir, '.env.local.template'), envTemplate, 'utf-8')
  logger.success('.env.local.template written')

  // 1g. Verify the scaffold compiles (exits cleanly unlike `npm run dev`)
  logger.step('Verifying scaffold builds...')
  try {
    await exec('npm run build', { cwd: appDir })
    logger.success('Build passed')
  } catch (err) {
    logger.warn(`Build check failed: ${err.message.split('\n')[0]}`)
    logger.info('  Fix build errors before proceeding, or continue and resolve later.')
  }

  logger.success(`Phase 1 complete — ${appDir}`)
  return { appDir, schema, migrationFile: inputs._migrationFile || null }
}

function buildEnvTemplate(inputs) {
  const { appName, services } = inputs
  const lines = [
    `# .env.local.template — ${appName}`,
    `# Generated by household-app-infrastructure`,
    `# cp .env.local.template .env.local && fill in values`,
    `# Never commit .env.local`,
    ``,
  ]

  if (services.includes('supabase')) {
    lines.push(
      `# ── Supabase ──────────────────────────────────────────────`,
      `VITE_SUPABASE_URL=`,
      `VITE_SUPABASE_ANON_KEY=`,
      ``,
    )
  }

  if (services.includes('anthropic')) {
    lines.push(
      `# ── Anthropic (server-side only — use via API route) ──────`,
      `ANTHROPIC_API_KEY=`,
      ``,
    )
  }

  if (services.includes('resend')) {
    lines.push(
      `# ── Resend (server-side only — use via API route) ─────────`,
      `RESEND_API_KEY=`,
      `RESEND_FROM_ADDRESS=`,
      ``,
    )
  }

  lines.push(
    `# ── App ───────────────────────────────────────────────────`,
    `VITE_APP_URL=`,
    ``,
    `# ── Test ──────────────────────────────────────────────────`,
    `# TEST_USER_EMAIL=`,
    `# TEST_USER_PASSWORD=`,
    ``,
  )

  return lines.join('\n')
}
