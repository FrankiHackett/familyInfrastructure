// phases/04-supabase.js — Apply migration via Supabase CLI, generate TypeScript types
// Only runs when services includes 'supabase'.

import { writeFileSync, mkdirSync } from 'node:fs'
import { join } from 'node:path'
import { logger } from '../lib/logger.js'
import { exec, toolExists } from '../lib/exec.js'
import { confirm } from '../lib/prompt.js'

export async function runSupabase(cfg, inputs, appDir, iface) {
  logger.phase('3', 'Supabase')

  if (!inputs.services.includes('supabase')) {
    logger.info('Supabase not in services — Phase 3 skipped')
    return null
  }

  // Guard: supabase CLI must be installed
  if (!(await toolExists('supabase'))) {
    throw new Error(
      'supabase CLI not found. Install: https://supabase.com/docs/guides/cli/getting-started'
    )
  }

  const schema      = inputs.appName.replace(/-/g, '_')
  const projectRef  = cfg.supabase.project_ref
  const accessToken = cfg.supabase.access_token

  // 3a. Verify migration file exists
  const migrationFile = inputs._migrationFile
  if (!migrationFile) {
    throw new Error('No migration file found. Run Phase 1 first.')
  }
  logger.step(`Migration file: ${migrationFile.replace(appDir + '/', '')}`)

  // 3b. Confirm before applying to live project
  logger.warn(`This will apply the migration to Supabase project: ${projectRef}`)
  const ok = await confirm('Apply migration to live Supabase project?', iface)
  if (!ok) throw new Error('Supabase migration cancelled by user.')

  // 3c. Initialise supabase project structure (idempotent)
  // supabase db push requires a supabase/ directory with migrations/ inside.
  // Phase 1 already wrote migrations to supabase/migrations/ — just need init + link.
  logger.step('Initialising supabase project structure...')
  await exec('supabase init --force', { cwd: appDir }).catch(() => {
    // --force overwrites existing config; ignore errors if already initialised
  })

  // 3d. Link to remote project
  // SUPABASE_ACCESS_TOKEN is the auth mechanism — passed via env, never in args
  logger.step(`Linking to Supabase project: ${projectRef}`)
  await exec(`supabase link --project-ref ${projectRef}`, {
    cwd: appDir,
    env: { SUPABASE_ACCESS_TOKEN: accessToken },
  })

  // 3e. Apply migrations via supabase db push
  logger.step('Applying migration via supabase db push...')
  await exec('supabase db push', {
    cwd: appDir,
    env: { SUPABASE_ACCESS_TOKEN: accessToken },
  })
  logger.success('Migration applied')

  // 3f. Generate TypeScript types — capture stdout, write to file in Node
  logger.step('Generating TypeScript types...')
  const typesDir = join(appDir, 'src', 'types')
  mkdirSync(typesDir, { recursive: true })
  const typesFile = join(typesDir, 'database.types.ts')

  const { stdout: typesSql } = await exec(
    `supabase gen types typescript --project-ref ${projectRef} --schema ${schema}`,
    { cwd: appDir, env: { SUPABASE_ACCESS_TOKEN: accessToken } }
  )
  writeFileSync(typesFile, typesSql, 'utf-8')
  logger.success(`Types generated: src/types/database.types.ts`)

  // 3e. Write Supabase client helper using Vite env vars
  const clientFile = join(appDir, 'src', 'lib', 'supabase.ts')
  mkdirSync(join(appDir, 'src', 'lib'), { recursive: true })
  writeFileSync(clientFile, buildSupabaseClient(schema), 'utf-8')
  logger.success('Supabase client helper written: src/lib/supabase.ts')

  // 3f. Commit generated files
  await exec('git add -A', { cwd: appDir })
  const { stdout: statusOut } = await exec('git status --porcelain', { cwd: appDir })
  if (statusOut) {
    await exec(
      `git commit -m "feat: add supabase types and client for ${schema}"`,
      { cwd: appDir }
    )
  } else {
    logger.info('Nothing new to commit — skipping supabase commit')
  }

  logger.success(`Phase 3 complete — schema: ${schema}`)
  return { schema, projectRef }
}

function buildSupabaseClient(schema) {
  return `// src/lib/supabase.ts — generated by household-app-infrastructure
import { createClient } from '@supabase/supabase-js'
import type { Database } from '../types/database.types'

const supabaseUrl  = import.meta.env.VITE_SUPABASE_URL
const supabaseKey  = import.meta.env.VITE_SUPABASE_ANON_KEY

if (!supabaseUrl || !supabaseKey) {
  throw new Error('Missing VITE_SUPABASE_URL or VITE_SUPABASE_ANON_KEY')
}

export const supabase = createClient<Database>(supabaseUrl, supabaseKey)

// Typed schema helper
export const db = {
  from: (table: string) => supabase.schema('${schema}').from(table),
}
`
}
